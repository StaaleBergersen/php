# UV Oppgave 07 – Størst volum på eske

## Oppgavebeskrivelse

Du skal lage en algoritme der du skal finne hva som gir størst volum på en eske du kan brette med et A4-ark (optimalisering). 

I denne oppgaven skal du:
- Lage en funksjon som beregner volumet av esken som beskrevet over.
- Funksjonen skal ta imot **høyde** på esken som innverdi (argument) og returnere **volum** på esken.

## Løsningsmetode

### 1. Dokumentasjon i pseudokode
Algoritmen skal først dokumenteres gjennom å skrive løsningen i pseudokode. Pseudokoden skal testes for å sikre korrekt logikk.

### 2. Implementasjon i Python
Etter at pseudokoden er bekreftet riktig, skal programmet skrives i Python. Bruk **Visual Studio Code (VS Code)** som utviklingsmiljø.

### 3. Opplasting av filer
Både pseudokoden (i en `.txt`-fil) og Python-koden (i en `.py`-fil) skal til slutt lastes opp i **Teams** under oppgaven: **UV oppg 07 – Størst volum på eske**.

## Krav
- Funksjonen skal være tydelig dokumentert.
- Pseudokoden skal gi en klar oversikt over hvordan algoritmen fungerer.
- Python-koden skal være optimalisert og lett for andre å lese.

